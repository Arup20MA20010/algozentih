ll factorials[100001] = {0};
ll power(ll a, ll b)
{
    ll ans = 1;
    while (b)
    {
        if (b & 1)
        {
            ans = ((ans * (a % MOD)) % MOD + MOD) % MOD;
        }
        a = (a * a) % MOD;
        b >>= 1;
    }
    return ans;
}
ll rem(ll a)
{
    return ((a % MOD) + MOD) % MOD;
}
ll addMOD(ll a, ll b)
{
    return ((a % MOD + b % MOD) % MOD + MOD) % MOD;
}
ll subMOD(ll a, ll b)
{
    return ((a % MOD - b % MOD) % MOD + MOD) % MOD;
}
ll mulMOD(ll a, ll b)
{
    return ((a % MOD) * (b % MOD)) % MOD;
}
ll inverseMOD(ll a)
{
    return rem(power(a, MOD - 2));
}
ll divideMOD(ll a, ll b)
{
    return mulMOD(a, inverseMOD(b));
}
ll gcd(ll a, ll b)
{
    if (a < b)
    {
        return gcd(b, a);
    }
    return b == 0 ? a : gcd(b, a % b);
}
void factorial()
{
    for (ll i = 1; i <= 100001; i++)
    {
        factorials[i] = mulMOD(factorials[i], i);
    }
}
ll ncr(ll n, ll r)
{
    return divideMOD(factorials[n], mulMOD(factorials[r], factorials[n - r]));
}
int k;
bool check(int x, vi v)
{
    int n = v.size();
    int head = 0, tail = x - 1;
    int cnt = 0;
    for (int i = head; i <= tail; i++)
    {
        if (v[i] == 0)
            cnt++;
    }
    if (cnt <= k)
        return true;
    while (tail < n)
    {
        if (v[head] == 0)
        {
            cnt--;
        }
        if (tail + 1 < n && v[tail + 1] == 0)
        {
            cnt++;
        }
        head++;
        tail++;
        if (cnt <= k)
            return true;
    }
    return false;
}
void solve()
{
    int n;
    cin >> n >> k;
    vi v(n, 0);
    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    int ans = 0;
    int lo = 0, hi = n;
    while (lo <= hi)
    {
        int mid = lo + (hi - lo) / 2;
        if (check(mid, v))
        {
            lo = mid + 1;
            ans = mid;
        }
        else
        {
            hi = mid - 1;
        }
    }
    cout << ans + 1 << nline;
}
