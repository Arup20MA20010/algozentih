ll factorials[100001] = {0};
ll power(ll a, ll b)
{
    ll ans = 1;
    while (b)
    {
        if (b & 1)
        {
            ans = ((ans * (a % MOD)) % MOD + MOD) % MOD;
        }
        a = (a * a) % MOD;
        b >>= 1;
    }
    return ans;
}
ll rem(ll a)
{
    return ((a % MOD) + MOD) % MOD;
}
ll addMOD(ll a, ll b)
{
    return ((a % MOD + b % MOD) % MOD + MOD) % MOD;
}
ll subMOD(ll a, ll b)
{
    return ((a % MOD - b % MOD) % MOD + MOD) % MOD;
}
ll mulMOD(ll a, ll b)
{
    return ((a % MOD) * (b % MOD)) % MOD;
}
ll inverseMOD(ll a)
{
    return rem(power(a, MOD - 2));
}
ll divideMOD(ll a, ll b)
{
    return mulMOD(a, inverseMOD(b));
}
ll gcd(ll a, ll b)
{
    if (a < b)
    {
        return gcd(b, a);
    }
    return b == 0 ? a : gcd(b, a % b);
}
void factorial()
{
    for (ll i = 1; i <= 100001; i++)
    {
        factorials[i] = mulMOD(factorials[i], i);
    }
}
ll ncr(ll n, ll r)
{
    return divideMOD(factorials[n], mulMOD(factorials[r], factorials[n - r]));
}
template <typename T>
class monotone_dequeue
{
public:
    deque<T> dq;
    void insert(T x)
    {
        while (!dq.empty() && x < dq.back())
        {
            dq.pop_back();
        }
        dq.push_back(x);
    }
    void remove(int x)
    {
        if (dq.front() == x)
        {
            dq.pop_front();
        }
    }
    T getMin()
    {
        if (!dq.empty())
        {
            return dq.front();
        }
    }
};
char board[8][8];
int ans;
bool check(int row, int col)
{
    if (board[row][col] == '*')
    {
        return false;
    }
    for (int prow = 0; prow < row; prow++)
    {
        int col_index = 0;
        for (int pcol = 0; pcol < 8; pcol++)
        {
            if (board[prow][pcol] == 'Q')
                col_index = pcol;
        }
        if (col == col_index || abs(col - col_index) == abs(row - prow))
        {
            return false;
        }
    }
    return true;
}
void rec(int level)
{
    // base case
    if (level == 8)
    {
        ans++;
        return;
    }
    // recursive case
    //  we go through each of the col and check if the queen can be placed or not
    for (int col = 0; col < 8; col++)
    {
        if (check(level, col))
        {
            board[level][col] = 'Q';
            rec(level + 1);
            board[level][col] = '.';
        }
    }
}
